import UIKit

extension UIColor {
    class var primary: UIColor {
        return UIColor(red: 70.0/255.0, green: 43.0/255.0, blue: 132.0/255.0, alpha: 1)
    }

    class var white: UIColor {
        return UIColor(white: 1, alpha: 1)
    }
}
